package org.nishen.resourcepartners;

public class ResourcePartnerSynchroniserException extends Exception
{
	private static final long serialVersionUID = -2153804468705212252L;

	public ResourcePartnerSynchroniserException()
	{
		super();
	}

	public ResourcePartnerSynchroniserException(String msg)
	{
		super(msg);
	}

	public ResourcePartnerSynchroniserException(Throwable t)
	{
		super(t);
	}
}
